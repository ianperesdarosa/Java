class Playground {
    public static void main(String[ ] args) {

    //Loop For    
        for(int i = 0; i < 10; i++) {
            System.out.println("Hello");
        }

    //Loop While
        int x = 0;
        while(x < 5) {
            System.out.println("Ian");
            x++;
        }

    //Loop Do While    
        int y = 0;
        do {
            System.out.println("True");
            y++;
        }while(y < 3);
    }    
}